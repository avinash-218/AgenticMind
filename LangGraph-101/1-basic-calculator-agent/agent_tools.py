def multiply(a: float, b: float) -> float:
    """Multiply two numbers.

    Args:
        a (float): operand 1
        b (float): operand 2

    Returns:
        float: output of multiplication
    """
    return a * b

def divide(a: float, b: float) -> float:
    """Divide two numbers.

    Args:
        a (float): operand 1
        b (float): operand 2

    Returns:
        float: output of multiplication
    """
    return a / b

def add(a: float, b: float) -> float:
    """Add two numbers.

    Args:
        a (float): operand 1
        b (float): operand 2

    Returns:
        float: output of addition
    """
    return a + b

def subtract(a: float, b: float) -> float:
    """Subtract two numbers.

    Args:
        a (float): operand 1
        b (float): operand 2

    Returns:
        float: output of subtraction
    """
    return a - b